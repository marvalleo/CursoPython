#rangos

for i in range(1,11):
    print(i) #imprime desde 1 hasta 10


for i in range(1,11, 3):
    print(i) #imprime desde 1 hasta 10 saltando de 3 en 3

for i in range(10,0,-1):
    print(i) #imprime desde 10 hasta 1 saltando de 1 en 1

for i in range(1,11):
    if i % 2 == 0:
        print(i) #imprime los numeros pares

for i in range(1,11):
    if i % 2 != 0:
        print(i) #imprime los numeros impares

for i in range(-10,0):
    print(i) #imprime los numeros negativos