import math
import random
print(math.pow(2,3))#2 elevado a 3
print(math.sqrt(9))#raiz cuadrada de 9
print(math.isqrt(9))#raiz cuadrada de 9
print(math.pi)#pi
print(math.e)#e
print(math.sin(math.pi/2))#seno de pi/2
print(math.cos(math.pi/2))#coseno de pi/2
print(math.tan(math.pi/4))#tangente de pi/4
print(math.asin(0.5))#arcoseno de 0.5
print(math.acos(0.5))#arcocoseno de 0.5
print(math.atan(0.5))#arctangente de 0.5
print(math.degrees(math.pi))#grados de pi
print(math.radians(180))#radianes de 180
print(math.log(100,10))#logaritmo de 100 en base 10
print(random.randint(1,10))#numero aleatorio entre 1 y 10